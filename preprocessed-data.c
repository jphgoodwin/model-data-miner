int tool_progress_cb(void *clientp,
                     curl_off_t dltotal, curl_off_t dlnow,
                     curl_off_t ultotal, curl_off_t ulnow)
{



  struct timeval now = tvnow();
  struct per_transfer *per = clientp;
  struct OperationConfig *config = per->config;
  struct ProgressData *bar = &per->progressbar;
  curl_off_t total;
  curl_off_t point;




  if(bar->initial_size < 0 ||
     ((0x7FFFFFFFFFFFFFFFL - bar->initial_size) < (dltotal + ultotal)))
    total = 0x7FFFFFFFFFFFFFFFL;
  else
    total = dltotal + ultotal + bar->initial_size;



  if(bar->initial_size < 0 ||
     ((0x7FFFFFFFFFFFFFFFL - bar->initial_size) < (dlnow + ulnow)))
    point = 0x7FFFFFFFFFFFFFFFL;
  else
    point = dlnow + ulnow + bar->initial_size;

  if(bar->calls) {

    if(total) {

      if(bar->prev == point)

        return 0;
      else if((tvdiff(now, bar->prevtime) < 100L) && point < total)

        return 0;
    }
    else {

      if(tvdiff(now, bar->prevtime) < 100L)

        return 0;
      fly(bar, point != bar->prev);
    }
  }


  bar->calls++;

  if((total > 0) && (point != bar->prev)) {
    char line[256 + 1];
    char format[40];
    double frac;
    double percent;
    int barwidth;
    int num;
    if(point > total)

      total = point;

    frac = (double)point / (double)total;
    percent = frac * 100.0;
    barwidth = bar->width - 7;
    num = (int) (((double)barwidth) * frac);
    if(num > 256)
      num = 256;
    memset(line, '#', num);
    line[num] = '\0';
    curl_msnprintf(format, sizeof(format), "\r%%-%ds %%5.1f%%%%", barwidth);
    curl_mfprintf(bar->out, format, line, percent);
  }
  fflush(bar->out);
  bar->prev = point;
  bar->prevtime = now;

  if(config->readbusy) {
    config->readbusy = bool_false;
    curl_easy_pause(per->curl, ((0)|(0)));
  }

  return 0;
}

